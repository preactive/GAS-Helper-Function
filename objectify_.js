function objectify_(array)
{
  // incoming Array: var array = [ [1,'a'], [2,'b'], [3,'c'] ];
  // Output  Object: { 1: "a", 2: "b", 3: "c" };
  // Note pull only 2 columns into the array.  will add code to expect that later.
    return array.reduce(function(p, c) {
         p[c[0]] = c[1];
         return p;
    }, {});
}
