// Pad a number with x amound of Zero

//example 'var PadWhseNum = pad_(IncomingWhseNum,5);'
//output would for "15" would be "00015"

function pad_(n, width, z)
{
  z = z || '0';
  n = n + '';
  return n.length >= width ? n : new Array(width - n.length + 1).join(z) + n;
}
